package Entity.Actor;

/**
 * The {@code Gender} enum represents gender categories for actors within the system.
 * It is used to specify the gender of individuals such as staff members or managers.
 * <p>
 * Currently, the system recognizes two gender categories:
 * <ul>
 *     <li>{@code MALE} - Represents the male gender.</li>
 *     <li>{@code FEMALE} - Represents the female gender.</li>
 * </ul>
 */
public enum Gender {
    MALE, FEMALE
}
